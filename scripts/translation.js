// Current language
let currentLang = 'fr'; // Default language is French

// Function to change the language
function changeLanguage(lang) {
    currentLang = lang;
    updateContent();
}

// Function to get the translation of a text
function getTranslation(text) {
    if (translations[currentLang] && translations[currentLang][text]) {
        return translations[currentLang][text];
    }
    return text; // Return original text if no translation found
}

// Function to update all translatable content
function updateContent() {
    // Find all elements with the data-translate attribute
    const translatableElements = document.querySelectorAll('[data-translate]');
    
    translatableElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (key) {
            element.textContent = getTranslation(key);
        }
    });
}

// Initialize the translation system when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    // Initialize by adding data-translate attributes to elements
    const elementsToTranslate = document.querySelectorAll('[data-translate-init]');
    
    elementsToTranslate.forEach(element => {
        const originalText = element.textContent.trim();
        element.setAttribute('data-translate', originalText);
    });
    
    // Setup language buttons
    const langButtons = document.querySelectorAll('.language-selector button');
    langButtons.forEach(button => {
        button.addEventListener('click', function() {
            const lang = this.getAttribute('onclick').match(/'([^']+)'/)[1];
            changeLanguage(lang);
        });
    });
    
    // Initial translation
    updateContent();
}); 